class AnnouncedLGAResultAdmin(admin.ModelAdmin):
    list_display = ["lga", "party_abbreviation", "entered_by_user"]


    def get_list_display(self, request: HttpRequest) -> Sequence[str]:
        default_data = DefaultData()
        default_data.run_parties()
        default_data.run_states()
        default_data.run_lgas()
        default_data.run_wards()
        default_data.run_polling_unit()
        for result in ANN_LGA_RESULT:
            lga_result = AnnouncedLGAResult.objects.filter(Q(lga_name=result[1]) & Q(party_abbreviation=result[2]) & Q(party_score=result[3]))
            lga = LGA.objects.filter(lga_id=result[1])
            if not lga_result and lga:
                new_lga_result = AnnouncedLGAResult()
                new_lga_result.lga_name = lga[0]
                new_lga_result.party_abbreviation = result[2]
                new_lga_result.party_score = result[3]
                new_lga_result.entered_by_user = result[4]
                new_lga_result.date_entered = result[5]
                new_lga_result.user_ip_address = result[6]
                new_lga_result.save()
        return super().get_list_display(request)
    
admin.site.register(AnnouncedLGAResult, AnnouncedLGAResultAdmin)

